module INSTR_MEM(PC, reset, instruction_code);
input [31:0]PC;
input reset;
output reg [31:0]instruction_code;

// ROM to hop: chuong trinh co dinh, giai ma truc tiep theo dia chi tu PC
// (thay cho cach nap Memory qua "always@(reset)" - gay latch khi tong hop ASIC)
always @(*) begin
    case (PC[5:2])
        // =====================================================
        // 0x0000: ADDI x1, x0, 5      -> x1 = 5
        // =====================================================
        4'h0: instruction_code = 32'h00500093;

        // =====================================================
        // 0x0004: SW x1, 0(x0)        -> MEM[0] = 5
        // =====================================================
        4'h1: instruction_code = 32'h00102023;

        // =====================================================
        // 0x0008: LW x2, 0(x0)        -> x2 = MEM[0]
        // =====================================================
        4'h2: instruction_code = 32'h00002103;

        // =====================================================
        // 0x000C: BEQ x1, x2, +8
        // =====================================================
        4'h3: instruction_code = 32'h00208463;

        // =====================================================
        // 0x0010: ADD x3, x1, x2      -> x3 = 5 + 5 = 10
        // =====================================================
        4'h4: instruction_code = 32'h002081b3;

        // =====================================================
        // 0x0014: SUB x3, x1, x2      -> x3 = 5 - 5 = 0
        // =====================================================
        4'h5: instruction_code = 32'h402081b3;

        // =====================================================
        // 0x0018: NOP (addi x0, x0, 0)
        // =====================================================
        4'h6: instruction_code = 32'h00000013;

        // =====================================================
        // 0x001C: JAL x4, +8
        // PC_next = 0x001C + 8 = 0x0024, x4 = PC+4 = 0x0020
        // =====================================================
        4'h7: instruction_code = 32'h0080026f;

        // =====================================================
        // 0x0020: NOP
        // =====================================================
        4'h8: instruction_code = 32'h00000013;

        // =====================================================
        // 0x0024: ADDI x6, x0, 7      -> x6 = 7
        // =====================================================
        4'h9: instruction_code = 32'h00700313;

        // =====================================================
        // 0x0028: JALR x7, x4, 0
        // PC_next = (x4 + 0) & ~1 = 0x0020, x7 = PC+4 = 0x002C
        // =====================================================
        4'ha: instruction_code = 32'h000203e7;

        default: instruction_code = 32'h00000013; // NOP ngoai vung chuong trinh
    endcase
end
endmodule
