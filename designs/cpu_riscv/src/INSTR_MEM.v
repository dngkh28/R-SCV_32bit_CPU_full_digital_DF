module INSTR_MEM(PC, instruction_code);
input [31:0]PC;
output [31:0]instruction_code;

reg [7:0]Memory[63:0];
assign instruction_code = {Memory[PC[5:0]+3], Memory[PC[5:0]+2],
                              Memory[PC[5:0]+1], Memory[PC[5:0]]};

initial begin
			// =====================================================
        // 0x0000:
        // ADDI x1, x0, 5
        // x1 = 5
        // =====================================================
        //
        // 000000000101_00000_000_00001_0010011
        // 0x00500093
        //
        Memory[3] = 8'h00;
        Memory[2] = 8'h50;
        Memory[1] = 8'h00;
        Memory[0] = 8'h93;


        // =====================================================
        // 0x0004:
        // STORE WORD
        // SW x1, 0(x0)
        // MEM[0] = 5
        // =====================================================
        //
        // 0000000_00001_00000_010_00000_0100011
        // 0x00102023
        //
        Memory[7] = 8'h00;
        Memory[6] = 8'h10;
        Memory[5] = 8'h20;
        Memory[4] = 8'h23;


        // =====================================================
        // 0x0008:
        // LOAD WORD
        // LW x2, 0(x0)
        // x2 = MEM[0]
        // =====================================================
        //
        // 000000000000_00000_010_00010_0000011
        // 0x00002103
        //
        Memory[11] = 8'h00;
        Memory[10] = 8'h00;
        Memory[9]  = 8'h21;
        Memory[8]  = 8'h03;
		  // =====================================================
        // 0x000C:
        // BRANCH
        // beq x1, x2, +8
        // =====================================================
        //
        // 0_000000_00010_00001_000_0100_0_1100011
        // 0x00208463
        //
		  Memory[15] = 8'h00;
        Memory[14] = 8'h20;
        Memory[13]  = 8'h84;
        Memory[12]  = 8'h63;
        // =====================================================
        // 0x0010:
        // ADD
        // ADD x3, x1, x2
        // x3 = 5 + 5 = 10
        // =====================================================
        //
        // 0000000_00010_00001_000_00011_0110011
        // 0x002081b3
        //
        Memory[19] = 8'h00;
        Memory[18] = 8'h20;
        Memory[17] = 8'h81;
        Memory[16] = 8'hb3;
		  // =====================================================
        // 0x00014:
        // SUB
        // sub x3, x1, x2
        // x3 = 5 - 5 = 0
        // =====================================================
        //
        // 0100000_00010_00001_000_00011_0110011
        // 0x402081B3
        Memory[23] = 8'h40;
        Memory[22] = 8'h20;
        Memory[21] = 8'h81;
        Memory[20] = 8'hb3;

        // =====================================================
        // 0x0018: NOP (addi x0, x0, 0)
        // =====================================================
        Memory[27] = 8'h00;
        Memory[26] = 8'h00;
        Memory[25] = 8'h00;
        Memory[24] = 8'h13;

        // =====================================================
        // 0x001C: JAL x4, +8
        // PC_next = 0x001C + 8 = 0x0024
        // x4 = PC+4 = 0x0020 (link register)
        // 0x0080026F
        // =====================================================
        Memory[31] = 8'h00;
        Memory[30] = 8'h80;
        Memory[29] = 8'h02;
        Memory[28] = 8'h6F;

        // =====================================================
        // 0x0020: NOP — omitted
        // =====================================================
        Memory[35] = 8'h00;
        Memory[34] = 8'h00;
        Memory[33] = 8'h00;
        Memory[32] = 8'h13;

        // =====================================================
        // 0x0024: ADDI x6, x0, 7
        // x6 = 7 
        // 0x00700313
        // =====================================================
        Memory[39] = 8'h00;
        Memory[38] = 8'h70;
        Memory[37] = 8'h03;
        Memory[36] = 8'h13;

        // =====================================================
        // 0x0028: JALR x7, x4, 0
        // PC_next = (x4 + 0) & ~1 = 0x0020
        // x7 = PC+4 = 0x002C 
        // 0x000203E7
        // =====================================================
        Memory[43] = 8'h00;
        Memory[42] = 8'h02;
        Memory[41] = 8'h03;
        Memory[40] = 8'hE7;

end
endmodule
